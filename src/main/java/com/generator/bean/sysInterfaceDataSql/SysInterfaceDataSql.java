package com.generator.bean.sysInterfaceDataSql;

public class SysInterfaceDataSql {

    private String sqlId;

    private String sqlBatchId;

    private String sql;

    private Integer id;

    public String getSqlId() {
        return sqlId;
    }

    public void setSqlId(String sqlId) {
        this.sqlId = sqlId;
    }

    public String getSqlBatchId() {
        return sqlBatchId;
    }

    public void setSqlBatchId(String sqlBatchId) {
        this.sqlBatchId = sqlBatchId;
    }

    public String getSql() {
        return sql;
    }

    public void setSql(String sql) {
        this.sql = sql;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}
